package org.guanzon.cas.tbjhandler.constant;

/**
 * TBJ_Constant ----------------------------------------------------------
 * Defines constant values for Transaction Book Journal (TBJ) status codes.
 * 
 * VOID = "0"       : Transaction is VOID
 * OPEN = "1"       : Transaction has OPEN
 * CONFIRMED = "2"  : Transaction has been CONFIRMED
 */
public class TBJ_Constant {

    /** Transaction is VOID */
    public static final String VOID = "0";

    /** Transaction is OPEN */
    public static final String OPEN = "1";
    
    /** Transaction has been CONFIRMED */
    public static final String CONFIRMED = "2";
}
